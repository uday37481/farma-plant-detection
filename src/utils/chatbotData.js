export const getChatbotResponse = (message, language = 'en') => {
  const lowerMessage = message.toLowerCase();
  const responses = chatbotResponses[language];

  if (lowerMessage.includes('hello') || lowerMessage.includes('hi') || lowerMessage.includes('hey') ||
      lowerMessage.includes('नमस्ते') || lowerMessage.includes('नमस्कार')) {
    return responses.greeting[Math.floor(Math.random() * responses.greeting.length)];
  }

  if (lowerMessage.includes('disease') || lowerMessage.includes('sick') || lowerMessage.includes('problem') ||
      lowerMessage.includes('रोग') || lowerMessage.includes('बीमारी')) {
    return responses.diseaseHelp[Math.floor(Math.random() * responses.diseaseHelp.length)];
  }

  if (lowerMessage.includes('fertilizer') || lowerMessage.includes('fertiliser') || lowerMessage.includes('nutrient') ||
      lowerMessage.includes('उर्वरक') || lowerMessage.includes('खत')) {
    return responses.fertilizerAdvice[Math.floor(Math.random() * responses.fertilizerAdvice.length)];
  }

  if (lowerMessage.includes('water') || lowerMessage.includes('irrigation') || lowerMessage.includes('irrigate') ||
      lowerMessage.includes('सिंचाई') || lowerMessage.includes('पाणी')) {
    return responses.irrigationTips[Math.floor(Math.random() * responses.irrigationTips.length)];
  }

  if (lowerMessage.includes('tomato') || lowerMessage.includes('टमाटर') || lowerMessage.includes('टोमॅटो')) {
    return responses.tomatoCare[Math.floor(Math.random() * responses.tomatoCare.length)];
  }

  if (lowerMessage.includes('onion') || lowerMessage.includes('प्याज') || lowerMessage.includes('कांदा')) {
    return responses.onionCare[Math.floor(Math.random() * responses.onionCare.length)];
  }

  if (lowerMessage.includes('rice') || lowerMessage.includes('चावल') || lowerMessage.includes('तांदूळ')) {
    return responses.riceCare[Math.floor(Math.random() * responses.riceCare.length)];
  }

  if (lowerMessage.includes('wheat') || lowerMessage.includes('गेहूं') || lowerMessage.includes('गव्हा')) {
    return responses.wheatCare[Math.floor(Math.random() * responses.wheatCare.length)];
  }

  if (lowerMessage.includes('pest') || lowerMessage.includes('insect') || lowerMessage.includes('bug') ||
      lowerMessage.includes('कीट') || lowerMessage.includes('कीड')) {
    return responses.pestControl[Math.floor(Math.random() * responses.pestControl.length)];
  }

  if (lowerMessage.includes('soil') || lowerMessage.includes('ground') || lowerMessage.includes('earth') ||
      lowerMessage.includes('मिट्टी') || lowerMessage.includes('माती')) {
    return responses.soilHealth[Math.floor(Math.random() * responses.soilHealth.length)];
  }

  return responses.default[Math.floor(Math.random() * responses.default.length)];
};